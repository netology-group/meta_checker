env :GEM_PATH, ENV['GEM_PATH']

set :output,
  error: "#{Whenever.path}/log/whenever_error.log",
  standard: "#{Whenever.path}/log/whenever_standard.log"

every :day, at: '2:00am' do
  command "cd #{Whenever.path} && bundle exec rspec"
end
